public class Personinfo {
    private String touxiang;
    private String name;
    private String sex;
    private String id;
    private String jiaoshao;
    public Personinfo(){}
    public Personinfo(String touxiang, String name, String sex, String id, String jiaoshao) {
        this.touxiang = touxiang;
        this.name = name;
        this.sex = sex;
        this.id = id;
        this.jiaoshao = jiaoshao;
    }
}
